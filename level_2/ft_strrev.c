char	*ft_strrev(char *str)
{
	char	swap;
	int		i;
	int		j;

	i = 0;
	while (str[i])
		i++;
	i--;
	j = 0;
	while (j <= i)
	{
		swap = str[i];
		str[i] = str[j];
		str[j] = swap;
		i--;
		j++;
	}
	return (str);
}