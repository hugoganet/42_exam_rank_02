#include <unistd.h>
#include <stdio.h>

int	main(int ac, char **av)
{
	int	i;
	int	j;
	
	if (ac == 2)
	{
		i = 0;
		while (av[1][i])
			i++;
		i--;
		while (i >= 0 && (av[1][i] == 32 || av[1][i] == 9))
			i--;
		j = i;
		while (j >= 0 && av [1][j] != 32 && av[1][j] != 9)
			j--;
		j++;
		while (j <= i)
			write(1, &av[1][j++], 1);
	}
	write(1, "\n", 1);
	return (0);
}
